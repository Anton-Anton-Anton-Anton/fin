<h2> My experience with these frameworks </h2>

<p>I switched to svelt after being too frustrated with React to continue.</p>

    <p>I found it much easier and much closer to the lowlevel development. </p>

        <p>The additions that Svelte provided were immediately clear, such as the props that can be passed into the components</p>

            <p>I liked the separation of CSS. Everything being scoped and controlled. But that very same aspect was hard to figure out in the beginning.
But in the end I liked that every component had its own css in the same file.</p>


<p>the fact that svelte has to compile the whole thing was confusing from the start, because i didn't realize that the blank file was a compilation error.</p>


    <p>I really liked the template logic very easy to follow for a programmer of how to structure the page, using the if statement logic.<p>

